export const getCaseMeta = (id) => CASES.find((c) => c.id === id) || null

// Порядковый номер дела в картотеке (для последовательного открытия)
export const caseIndex = (id) => CASES.findIndex((c) => c.id === id)

// Загрузка дела целиком: обложка + данные
export async function loadCase(id) {
  const meta = getCaseMeta(id)
  if (!meta) return null
  const mod = await meta.load()
  return { ...meta, ...mod.default }
}
